public class Fourth extends Declension
	{
	Fourth()
		{
		name="fourth";
		}
	public static void isMasculineFo()
		{
		System.out.println("Fourth Declension");
		System.out.println(root+"us"+ "         "+root+"us");
		System.out.println(root+"us"+ "        "+root+"uum");
		System.out.println(root+"ui"+ "        "+root+"ibus");
		System.out.println(root+"um"+ "        "+root+"us");
		System.out.println(root+"u"+ "        "+root+"ibus");
		System.out.println("~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~");
		}
	public static void isNeuterFo()
		{
		System.out.println("Fourth Declension Neuter");
		System.out.println(root+"u"+ "         "+root+"ua");
		System.out.println(root+"us"+ "        "+root+"uum");
		System.out.println(root+"u"+ "        "+root+"ibus");
		System.out.println(root+"u"+ "        "+root+"ua");
		System.out.println(root+"u"+ "        "+root+"ibus");
		System.out.println("~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~");
		}
	}